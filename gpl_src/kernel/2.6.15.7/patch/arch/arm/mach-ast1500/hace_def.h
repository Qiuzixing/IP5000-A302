		#define ENABLE_LOAD_CONTEXT	0x00000000
	#define DISABLE_DATA_OPERATION_MASK	0x00000800
		#define DISABLE_DATA_OPERATION 0x00000800
		#define ENABLE_DATA_OPERATION 0x00000000
	#define CRYPTO_ENGINE_SELECT_MASK 0x00010000
		#define AES_ENGINE 0x00000000
#define HAC_ENGINE_STATUS	0x1C
	#define CRYPTO_ENGINE_STATUS_MASK	0x00000002
		#define CRYPTO_ENGINE_BUSY	0x00000002
		#define CRYPTO_ENGINE_IDLE	0x00000000

#endif
